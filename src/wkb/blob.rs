use crate::{
    G, MyError,
    geom::{Geometries, Line, Lines, Point, Points, Polygon, Polygons, XY1V, XY2V, XY3V, XY4V},
    srid::SRID,
    wkb::{ByteOrder, Envelope, GeoPackageBinaryHeader, double, unsigned},
};
use tracing::error;

/// GeoPackage binary encoded geometry instance.
#[derive(Debug)]
pub(crate) struct StandardGeoPackageBinary {
    header: GeoPackageBinaryHeader,
    geom: G,
}

impl StandardGeoPackageBinary {
    /// Return the inner geometry object.
    pub(crate) fn geom(self) -> G {
        self.geom
    }

    /// Convenience method that returns the SRID of this instance.
    #[allow(dead_code)]
    pub(crate) fn srid(&self) -> &SRID {
        self.header.srid()
    }

    /// Convenience method that returns the envelope of this instance.
    #[allow(dead_code)]
    pub(crate) fn envelope(&self) -> &Envelope {
        self.header.envelope()
    }
}

impl TryFrom<&[u8]> for StandardGeoPackageBinary {
    type Error = MyError;

    fn try_from(ba: &[u8]) -> Result<Self, Self::Error> {
        // parse the header...
        let header = GeoPackageBinaryHeader::try_from(ba)?;
        let start = header.len();
        let (geom, _span) = wkb_geometry(ba, start, &header)?;

        Ok(Self { header, geom })
    }
}

// WKBGeometry is a union of many structures all of them start w/ the following
// * 1-byte: byte order (endianness),
// * 4-byte uint32: a key indicating what type of geometry structure follows
fn wkb_geometry(
    ba: &[u8],
    start: usize,
    header: &GeoPackageBinaryHeader,
) -> Result<(G, usize), MyError> {
    let mut pos = start;
    let byte_order = &ByteOrder::from(ba[pos]);
    pos += 1;
    let geom_type = unsigned(byte_order, ba, pos)?;
    pos += 4;
    match geom_type {
        1 => {
            let (xy, span) = point(byte_order, ba, pos)?;
            header.check_point(&xy)?;
            let g = Point::from_xy_and_srid(xy, header.srid().to_owned());
            Ok((G::Point(g), span))
        }
        2 => {
            let (xy, span) = line(byte_order, ba, pos)?;
            header.check_line(&xy)?;
            let g = Line::from_xy_and_srid(xy, header.srid().to_owned());
            Ok((G::Line(g), span))
        }
        3 => {
            let (xy, span) = polygon(byte_order, ba, pos)?;
            header.check_polygon(&xy)?;
            let g = Polygon::from_xy_and_srid(xy, header.srid().to_owned());
            Ok((G::Polygon(g), span))
        }
        4 => {
            let (xy, span) = points(byte_order, ba, pos)?;
            header.check_points(&xy)?;
            let g = Points::from_xy_and_srid(xy, header.srid().to_owned());
            Ok((G::Points(g), span))
        }
        5 => {
            let (xy, span) = lines(byte_order, ba, pos)?;
            header.check_lines(&xy)?;
            let g = Lines::from_xy_and_srid(xy, header.srid().to_owned());
            Ok((G::Lines(g), span))
        }
        6 => {
            let (xy, span) = polygons(byte_order, ba, pos)?;
            header.check_polygons(&xy)?;
            let g = Polygons::from_xy_and_srid(xy, header.srid().to_owned());
            Ok((G::Polygons(g), span))
        }
        7 => {
            let srs_id = header.srid();
            let (xy, span) = collection(byte_order, ba, pos, header)?;
            let g = Geometries::from_items_and_srid(xy, srs_id.to_owned());
            Ok((G::Vec(g), span))
        }
        x => {
            let msg = format!("Unsupported ({x}) geometry type");
            error!("{msg}");
            Err(MyError::Runtime(msg.into()))
        }
    }
}

// a macro to generate blob parsing for the 3 basic geometry types.
macro_rules! gen_wkb_functions {
    ($suffix:expr, $wkb_type:expr) => {
        paste::paste! {
            fn [<wkb_ $suffix>](ba: &[u8], start: usize) -> Result<([<XY $wkb_type V>], usize), MyError> {
                let mut pos = start;
                let byte_order = &ByteOrder::from(ba[pos]);
                pos += 1;
                let geom_type = unsigned(byte_order, ba, pos)?;
                pos += 4;
                let type_ = $wkb_type;
                assert_eq!(
                    geom_type, $wkb_type,
                    "Expected wkbType to be {type_} but found {geom_type}"
                );
                let (xy, offset) = [<$suffix>](byte_order, ba, pos)?;
                Ok((xy, 5 + offset))
            }
        }
    };
}

gen_wkb_functions!("point", 1);
gen_wkb_functions!("line", 2);
gen_wkb_functions!("polygon", 3);

// A pair of x y (double) coordinates.
// fn point(bo: &ByteOrder, ba: &[u8], start: usize) -> Result<(Vec<f64>, usize), MyError> {
fn point(bo: &ByteOrder, ba: &[u8], start: usize) -> Result<(Vec<f64>, usize), MyError> {
    let x = double(bo, ba, start)?;
    let mut span = 8;
    let y = double(bo, ba, start + span)?;
    span += 8;
    let xy = vec![x, y];
    Ok((xy, span))
}

// An unsigned `numPoints` followed by as many pairs of x, y coordinates.
fn line(bo: &ByteOrder, ba: &[u8], start: usize) -> Result<(XY2V, usize), MyError> {
    let num_points = unsigned(bo, ba, start)?;
    let mut span = 4;
    let mut xy: XY2V = Vec::with_capacity(usize::try_from(num_points)?);
    for _ in 0..num_points {
        let (coord, offset) = point(bo, ba, start + span)?;
        xy.push(coord);
        span += offset;
    }
    Ok((xy, span))
}

// An unsigned `numRings` followed by as many linear-rings. For our purposes a
// linear ring is structurally the same as a `line`.
fn polygon(bo: &ByteOrder, ba: &[u8], start: usize) -> Result<(XY3V, usize), MyError> {
    let num_rings = unsigned(bo, ba, start)?;
    let mut span = 4;
    let mut xy: XY3V = Vec::with_capacity(usize::try_from(num_rings)?);
    for _ in 0..num_rings {
        let (coord, offset) = line(bo, ba, start + span)?;
        xy.push(coord);
        span += offset;
    }
    Ok((xy, span))
}

// An unsigned `numPoints` followed by as many `wkb_point`s.
fn points(bo: &ByteOrder, ba: &[u8], start: usize) -> Result<(Vec<Vec<f64>>, usize), MyError> {
    let num_points = unsigned(bo, ba, start)?;
    let mut span = 4;
    let mut xy: Vec<Vec<f64>> = Vec::with_capacity(usize::try_from(num_points)?);
    for _ in 0..num_points {
        let (coord, offset) = wkb_point(ba, start + span)?;
        xy.push(coord);
        span += offset;
    }
    Ok((xy, span))
}

fn lines(bo: &ByteOrder, ba: &[u8], start: usize) -> Result<(XY3V, usize), MyError> {
    let num_lines = unsigned(bo, ba, start)?;
    let mut span = 4;
    let mut xy: XY3V = Vec::with_capacity(usize::try_from(num_lines)?);
    for _ in 0..num_lines {
        let (coord, offset) = wkb_line(ba, start + span)?;
        xy.push(coord);
        span += offset;
    }
    Ok((xy, span))
}

fn polygons(bo: &ByteOrder, ba: &[u8], start: usize) -> Result<(XY4V, usize), MyError> {
    let num_polygons = unsigned(bo, ba, start)?;
    let mut span = 4;
    let mut xy: XY4V = Vec::with_capacity(usize::try_from(num_polygons)?);
    for _ in 0..num_polygons {
        let (coord, offset) = wkb_polygon(ba, start + span)?;
        xy.push(coord);
        span += offset;
    }
    Ok((xy, span))
}

fn collection(
    bo: &ByteOrder,
    ba: &[u8],
    start: usize,
    header: &GeoPackageBinaryHeader,
) -> Result<(Vec<G>, usize), MyError> {
    let num_geometries = unsigned(bo, ba, start)?;
    let mut span = 4;
    let mut xy: Vec<G> = Vec::with_capacity(usize::try_from(num_geometries)?);
    for _ in 0..num_geometries {
        let (g, offset) = wkb_geometry(ba, start + span, header)?;
        xy.push(g);
        span += offset;
    }
    Ok((xy, span))
}

